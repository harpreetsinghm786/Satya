package com.official.pb.satya;

public class SliderItem {

    String des;
    int image;

    SliderItem(){

    }

    public SliderItem(String des, int image) {
        this.des = des;
        this.image = image;
    }

    public String getDes() {
        return des;
    }


    public int getImage() {
        return image;
    }

}
